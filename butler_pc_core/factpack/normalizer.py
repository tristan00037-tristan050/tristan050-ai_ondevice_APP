"""
Butler Fact Pack — Korean query normalizer

매칭 정확도를 위한 정규화:
  - 전각/반각 통일
  - 공백 압축
  - 한글 자모 분리 정상화 (NFC)
  - 한글 숫자 표기 통일 ("4대" / "사대" → "4대")
  - 흔한 오타·줄임말 보정 (제한적, 안전한 매핑만)

원칙: 정규화는 매칭 비교 시점에만 적용. 원본 쿼리는 보존하여 감사 로그에 기록.
"""

from __future__ import annotations

import re
import unicodedata


# 한글 숫자 → 아라비아 숫자 (안전한 매핑만)
_KOREAN_NUMERAL_MAP = {
    "사대": "4대",
    "오대": "5대",
    "삼대": "3대",
    "이대": "2대",
    "일대": "1대",
}

# 흔한 띄어쓰기 변형 통일
_SPACING_PAIRS = [
    ("4 대 보험", "4대보험"),
    ("4대 보험", "4대보험"),
    ("사 대 보험", "4대보험"),
    ("사대 보험", "4대보험"),
    ("최저 임금", "최저임금"),
    ("최저 시급", "최저시급"),
    ("부가 가치세", "부가가치세"),
    ("법인 세", "법인세"),
    ("종합 소득세", "종합소득세"),
    ("근로 기준법", "근로기준법"),
    ("주휴 수당", "주휴수당"),
    ("퇴직 금", "퇴직금"),
    ("연차 휴가", "연차휴가"),
    ("야근 수당", "야근수당"),
    ("산재 보험", "산재보험"),
    ("고용 보험", "고용보험"),
    ("국민 연금", "국민연금"),
    ("건강 보험", "건강보험"),
    ("장기요양 보험", "장기요양보험"),
]

# 동의어 통일 (의미 보존되는 것만)
_SYNONYM_PAIRS = [
    ("사대보험", "4대보험"),
    ("4대보험료", "4대보험"),
    ("부가세", "부가가치세"),
    ("vat", "부가가치세"),
    ("법인세율", "법인세"),
    ("종소세", "종합소득세"),
    ("근기법", "근로기준법"),
]


def normalize(text: str) -> str:
    """매칭 비교용 정규화 텍스트 반환."""
    if not text:
        return ""

    # 1. 유니코드 NFC 정규화 (자모 분리 → 완성형)
    s = unicodedata.normalize("NFC", text)

    # 2. 소문자화 (영문 부분만 영향)
    s = s.lower()

    # 3. 연속 공백 → 단일 공백 (먼저 수행해야 spacing pair 규칙이 매칭됨)
    s = re.sub(r"\s+", " ", s).strip()

    # 4. 한글 숫자 → 아라비아
    for k, v in _KOREAN_NUMERAL_MAP.items():
        s = s.replace(k, v)

    # 5. 띄어쓰기 통일
    for src, dst in _SPACING_PAIRS:
        s = s.replace(src, dst)

    # 6. 동의어 통일
    for src, dst in _SYNONYM_PAIRS:
        s = s.replace(src, dst)

    # 7. 끝부분 의문/조사 정리 (매칭 정확도 향상)
    s = re.sub(r"[?？！!.,。]+$", "", s).strip()

    return s


def keyword_in_query(keyword: str, normalized_query: str) -> bool:
    """키워드가 정규화된 쿼리에 포함되는지 확인. 키워드도 정규화하여 비교."""
    return normalize(keyword) in normalized_query


# ─────────────────────────────────────────────────────────────────────
# Light stemming — 한국어 어절 끝 조사·어미 제거 (매칭 정확도 향상)
# ─────────────────────────────────────────────────────────────────────
# 매처 토큰 비교 시점에만 적용. 정규화 출력 자체에는 영향 없음.
# 형태소 분석기(KoNLPy 등) 없이 보수적으로 처리.
# 길이 ≥ 3인 토큰의 끝에서만 잘라 "한국" 같은 짧은 명사가 손상되지 않도록 함.

# 긴 것부터 매칭 (greedy). 어미·조사·종결형 혼합.
_PARTICLE_SUFFIXES: tuple = (
    # 종결형 (의문/평서)
    "입니까", "입니다", "이에요", "이예요", "에요", "예요",
    "인가요", "인지요", "인지", "인가",
    "있나요", "있어요", "어떤가요", "어때요",
    "주세요", "주십시오", "주실래요", "줄래요", "줄래", "주실까요", "주시겠어요",
    "알려줘", "알려주세요", "알려주실래요",
    # 어미·조사 (긴 순)
    "에서", "에게", "에서의", "으로써", "으로서", "으로부터",
    "한테", "보다", "처럼", "마저", "조차", "마다", "라도", "만큼",
    "까지", "부터", "하고", "이라", "라는", "라고",
    "으로", "이고", "이며",
    # 1글자 조사 (가장 흔한 변형)
    "은", "는", "이", "가", "을", "를", "의", "에", "도", "만",
    "와", "과", "로", "야", "라", "나", "고",
)


def stem_token(token: str) -> str:
    """단일 토큰에서 흔한 한국어 조사/어미를 끝에서 제거.

    안전 가드:
      - 토큰 길이 < 3 이면 손대지 않음 (한국, 보험 등 보호)
      - 어미 제거 후 잔여 길이 ≥ 2 여야만 적용
      - 한 번만 제거 (재귀 X)
    """
    if not token or len(token) < 3:
        return token
    # 길이가 긴 어미부터 시도 (greedy)
    for suf in _PARTICLE_SUFFIXES:
        if token.endswith(suf):
            stripped = token[: -len(suf)]
            if len(stripped) >= 2:
                return stripped
    return token


def stem_tokens(text: str) -> set:
    """공백 분절 후 각 토큰에 stem_token 적용. set 반환."""
    return {stem_token(tok) for tok in text.split() if tok}
